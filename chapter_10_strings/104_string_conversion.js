//String Conversion

//To String
(200).toString(); //"200"
true.toString(); //"true"
null.toString(); //"null"
undefined.toString(); //"undefined"
([1, 2, 3]).toString(); //"1,2,3"
({ name: "John" }).toString(); //"[object Object]"

//To Number
Number("42"); //42

//ParseInt
console.log(parseInt("42px")); //42
console.log(parseInt("3.14rem")); //


//ParseFloat
console.log(parseFloat("3.14rem")); //3.14
