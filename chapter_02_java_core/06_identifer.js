//Identifier, literals, keywords, Operators

var a = 10;
a = 20;
console.log(a);
//output:20

//Variable name: a (Identifier) (name of the container)
//Variable value: 10 (Literal)
//Assignment operator: =
//var is a keyword

//Rules of Identifiers
//1. It should start with a letter, underscore or dollar sign
//2. It can contain letters, numbers, underscores or dollar sign
//3. It is case sensitive
//4. It should not be a reserved keyword
//5. It should be meaningful
//6. Cannot contain spaces
//7. Cannot start with number

//Example of valid identifiers
//a
//_a
//$a
//a1
//a_1
//a$1
//a_1$1
//a_1$1_1
var Name1 = "Sameer";
var name2 = "Sharma";
console.log(Name1); //case sensitive
console.log(name2);// case sensitive

